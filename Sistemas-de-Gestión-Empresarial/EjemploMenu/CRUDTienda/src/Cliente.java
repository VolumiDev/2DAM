
public class Cliente {
	private int id;
	private String nombre;
	private String ciudad;
	private double deuda;
	private boolean potencial;
	
	public Cliente(int id, String nombre, String ciudad, double deuda, boolean potencial) {
		this.id = id;
		this.nombre = nombre;
		this.ciudad = ciudad;
		this.deuda = deuda;
		this.potencial = potencial;
	}
	
	
	
}
