
public class User {

	private String name;
	private String date;
	private String sex;
	private double q1;
	private double q2;
	private double q3;
	private double q4;
	private double score;
	private int tries;
	
	public User(String name, String date, String sex, double q1, double q2, double q3, double q4, double score,
			int tries) {
		
		this.name = name;
		this.date = date;
		this.sex = sex;
		this.q1 = q1;
		this.q2 = q2;
		this.q3 = q3;
		this.q4 = q4;
		this.score = score;
		this.tries = tries;
	}
	
	
	
}
